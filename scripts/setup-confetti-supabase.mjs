#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const envPath = path.join(root, ".env.local");

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const item = argv[index];
    if (!item.startsWith("--")) continue;
    const key = item.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith("--")) {
      args[key] = "true";
    } else {
      args[key] = next;
      index += 1;
    }
  }
  return args;
}

function usage() {
  console.log(`
Confetti Supabase setup

Usage:
  npm run supabase:setup -- --url https://YOUR-PROJECT.supabase.co --anon YOUR_PUBLIC_ANON_OR_PUBLISHABLE_KEY

Optional:
  --name Confetti

This writes .env.local for the frontend. Do not pass service_role or secret keys here.
`);
}

function assertPublicKey(value) {
  const lower = value.toLowerCase();
  if (lower.includes("service_role") || lower.includes("secret")) {
    throw new Error("Refusing to write a service_role/secret key into frontend .env.local.");
  }
}

function readExistingEnv() {
  if (!fs.existsSync(envPath)) return {};
  return Object.fromEntries(
    fs
      .readFileSync(envPath, "utf8")
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith("#") && line.includes("="))
      .map((line) => {
        const [key, ...rest] = line.split("=");
        return [key, rest.join("=")];
      })
  );
}

const args = parseArgs(process.argv.slice(2));

if (args.help || !args.url || !args.anon) {
  usage();
  process.exit(args.help ? 0 : 1);
}

let projectUrl;
try {
  projectUrl = new URL(args.url);
} catch {
  throw new Error("--url must be a valid Supabase project URL.");
}

if (!projectUrl.hostname.endsWith(".supabase.co")) {
  throw new Error("--url should look like https://PROJECT-REF.supabase.co");
}

assertPublicKey(args.anon);

const existing = readExistingEnv();
const nextEnv = {
  ...existing,
  VITE_SUPABASE_URL: projectUrl.origin,
  VITE_SUPABASE_ANON_KEY: args.anon,
  VITE_SUPABASE_PROJECT_NAME: args.name ?? existing.VITE_SUPABASE_PROJECT_NAME ?? "Confetti"
};

const output = [
  "# Generated by npm run supabase:setup",
  "# Frontend-safe Supabase values only. Never add service_role here.",
  ...Object.entries(nextEnv).map(([key, value]) => `${key}=${value}`)
].join("\n");

fs.writeFileSync(envPath, `${output}\n`);

console.log(`Wrote ${envPath}`);
console.log("Next: restart the Vite dev server, then run npm run supabase:verify");
